enum custom_keycodes {
  PRIMARY = SAFE_RANGE,
  TXT_COPY,
  TXT_PSTE,
  QUOTE,
  DBQUOTE,
  TILD
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
   switch (keycode) {
   case TXT_COPY:
      if (record->event.pressed) {
         SEND_STRING(SS_LSFT(SS_LCTL("c")));
      }
      break;

   case TXT_PSTE:
      if (record->event.pressed) {
         SEND_STRING(SS_LCTL(SS_LSFT("v")));
      }
      break;

   case QUOTE:
      if (record->event.pressed) {
         SEND_STRING("' ");
      }
      break;

   case DBQUOTE:
      if (record->event.pressed) {
         SEND_STRING(SS_LSFT("' "));
      }
      break;

   case TILD:
      if (record->event.pressed) {
         SEND_STRING(SS_LSFT("` "));
      }
      break;

   }

   return true;

};

